#!/usr/bin/env python3
"""
This module provides a function to return the floor of a floating-point number.
"""


import math


def floor(n: float) -> int:
    """
    Returns the floor of a floating-point number.

    Args:
        n (float): The floating-point number.

    Returns:
        int: The floor of the number.
    """
    return math.floor(n)
