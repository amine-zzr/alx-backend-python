#!/usr/bin/env python3
"""
This module provides a function to return the string representation of a float.
"""


def to_str(n: float) -> str:
    """
    Converts a floating-point number to its string representation.

    Args:
        n (float): The floating-point number.

    Returns:
        str: The string representation of the number.
    """
    return str(n)
